export let regulars = {
  login: /^(\w+){3,32}$/,
  password: /^(?=.*[a-z])(?=.*[A-Z])(?=.*[0-9]).{6,}$/,
  age: /^(?:1(?:00?|\d)|[2-5]\d|[6-9]\d?)$/,
  email:
    /^([a-z0-9_-]+\.)*[a-z0-9_-]+@[a-z0-9_-]+(\.[a-z0-9_-]+)*\.[a-z]{2,6}$/,
  phone: /^(\s*)?(\+)?([- _():=+]?\d[- _():=+]?){12}(\s*)?$/,
};
